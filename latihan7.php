<?php
// Latihan Switch PHP //
$hewan = "kucing";

switch($hewan){
    case "kucing":
        echo "hewan kesukaan kamu adalah kucing";
        break;
    case "paus":
        echo "hewan kesukaan kamu adalah paus";
        break;
    default:
        echo "hewan kesukaan kamu antara kucing dan paus";
}
?>